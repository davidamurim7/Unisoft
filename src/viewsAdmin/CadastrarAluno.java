    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jTMatricula.getText().trim().equals("")
        || jTNome.getText().trim().equals("")
        || jTCpf.getText().trim().length() != 14
        || jTTelefone.getText().trim().length() != 14
        || jDData.getDate() == null
        || jPSenha.getText().trim().equals("")
        ){
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente todos os campos de texto", "ERRO", JOptionPane.ERROR_MESSAGE);
        }else{
            Connection conexao = Conexao.conectar();
            AlunoDAO alunoDAO = new AlunoDAO(conexao);
            Aluno aluno = new Aluno(
                    0,
                    jTMatricula.getText(),
                    jTNome.getText(),
                    jPSenha.getText(),
                    jTCpf.getText(),
                    jDData.getDate(),
                    jTTelefone.getText(),
                    new CursoDAO(conexao).retornaIdCurso((String) jCCurso.getSelectedItem())
            );

            if(!alunoDAO.existeAluno(aluno.getMatricula())){
                alunoDAO.addAluno(aluno);
                limpar();
                telaAdministrador.preencheTabelaAluno();
            }else{
                JOptionPane.showMessageDialog(null, "Aluno j√° cadastrado!");
            }

            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

