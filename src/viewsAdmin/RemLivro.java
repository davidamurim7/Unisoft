    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        if(jTIsbn.getText().trim().equals("")){
            limpar();
            JOptionPane.showMessageDialog(rootPane, "Campo \"ISBN\" vazio!","ERRO",JOptionPane.ERROR_MESSAGE);
        }else{
            Connection conexao = Conexao.conectar();
            LivroDAO livroDAO = new LivroDAO(conexao);
            if(livroDAO.existeLivro(jTIsbn.getText().trim())){
                preencheCampos(jTIsbn.getText().trim());
            }else{
                limpar();
                JOptionPane.showMessageDialog(rootPane, "Livro não cadastrado!","ERRO",JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_buscarActionPerformed

    private void jTIsbnKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTIsbnKeyReleased
        if((!salvaIsbn.equals(""))){
            if(!jTIsbn.getText().trim().equals(salvaIsbn))
                jTIsbn.setForeground(Color.red);
            else
                jTIsbn.setForeground(Color.black);
        }
    }//GEN-LAST:event_jTIsbnKeyReleased

