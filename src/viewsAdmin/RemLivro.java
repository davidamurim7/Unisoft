    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        if(salvaIsbn.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Livro não selecionado!","ERRO",JOptionPane.ERROR_MESSAGE);
        }else{
            Connection conexao = Conexao.conectar();
            LivroDAO livroDAO = new LivroDAO(conexao);
            Livro livro = new Livro();
            livro.setId(livroDAO.retornaIdLivro(salvaIsbn));
            if(livroDAO.delLivro(livro)){
                limpar();
                telaAdministrador.preencheTabelaLivros();
                JOptionPane.showMessageDialog(rootPane, "Livro excluído com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se o Livro não está sendo usado pelo sistema!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_confirmarActionPerformed

