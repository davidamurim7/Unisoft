    public void preencheCampos(String isbn){
        jTIsbn.setForeground(Color.black);
        salvaIsbn = isbn;
        Connection conexao = Conexao.conectar();
        LivroDAO livroDAO = new LivroDAO(conexao);
        Livro livro = livroDAO.selecionarLivro(livroDAO.retornaIdLivro(isbn));
        jLTitulo.setText(livro.getTitulo());
        jLAutor.setText(livro.getAutor());
        jLDescricao.setText(livro.getDescricao());
        jTImagem.setIcon(new ImageIcon(new ImageIcon(livro.getImagem()).getImage().getScaledInstance(189,194, Image.SCALE_DEFAULT)));  
        Conexao.desconectar(conexao);
    }
    
    public void limpar(){
        salvaIsbn = "";
        jTIsbn.setText("");
        jLTitulo.setText(" - ");
        jLDescricao.setText(" - ");
        jLAutor.setText(" - ");
        jTImagem.setIcon(null);
        jTIsbn.requestFocus();
        jTIsbn.setForeground(Color.black);
    }

