    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        if(salvaMatricula.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Aluno n√£o selecionado!","ERRO",JOptionPane.ERROR_MESSAGE);
        }else{
            Connection conexao = Conexao.conectar();
            AlunoDAO alunoDAO = new AlunoDAO(conexao);
            Aluno aluno = new Aluno();
            aluno.setId(alunoDAO.retornaIdAluno(salvaMatricula));
            if(alunoDAO.delAluno(aluno)){
                limpar();
                telaAdministrador.preencheTabelaAluno();
                JOptionPane.showMessageDialog(rootPane, "Aluno exclu√≠do com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Verifique se o Aluno n√£o est√° sendo usado pelo sistema!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_confirmarActionPerformed

