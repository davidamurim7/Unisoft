    private void jTMatriculaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTMatriculaKeyReleased
        if((!salvaMatricula.equals(""))){
            if(!jTMatricula.getText().trim().equals(salvaMatricula))
                jTMatricula.setForeground(Color.red);
            else
                jTMatricula.setForeground(Color.black);
        }
    }//GEN-LAST:event_jTMatriculaKeyReleased

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
        if(jTMatricula.getText().trim().equals("")){
            limpar();
            JOptionPane.showMessageDialog(rootPane, "Campo \"Matr√≠cula\" vazio!","ERRO",JOptionPane.ERROR_MESSAGE);
        }else{
            Connection conexao = Conexao.conectar();
            AlunoDAO alunoDAO = new AlunoDAO(conexao);
            if(alunoDAO.existeAluno(jTMatricula.getText().trim())){
                preencheCampos(jTMatricula.getText().trim());
            }else{
                limpar();
                JOptionPane.showMessageDialog(rootPane, "Aluno n√£o cadastrado!","ERRO",JOptionPane.ERROR_MESSAGE);
            }
            Conexao.desconectar(conexao);
        }
    }//GEN-LAST:event_buscarActionPerformed

