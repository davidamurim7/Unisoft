    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Connection conexao = Conexao.conectar();
        AlunoDAO alunoDAO = new AlunoDAO(conexao);
        AdminDAO adminDAO = new AdminDAO(conexao);
        
        Aluno al = alunoDAO.loginAluno(jTMatricula.getText(), jPSenha.getText());
        Admin adm = adminDAO.loginAdmin(jTMatricula.getText(), jPSenha.getText());
        if(al!=null){
            Principal p = new Principal(al);
            p.setVisible(true);
            this.dispose();            
        }else if(adm!=null){
            Administrador admin = new Administrador();
            admin.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Credenciais n√£o encontrada!", "ERRO", JOptionPane.ERROR_MESSAGE);

        }
        jTMatricula.setText("");
        jPSenha.setText("");
    }//GEN-LAST:event_jButton6ActionPerformed

